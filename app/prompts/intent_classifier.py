# インテント分類プロンプト

INTENT_CLASSIFICATION_PROMPT = """\
あなたはユーザーのメッセージを分類するアシスタントです。
以下のインテントから最も適切なものを1つだけ選び、そのインテント名のみを返してください。

【インテント一覧】
- schedule_check: 特定の日や月の予定を確認（例: 「今日の予定」「明日の予定教えて」「明後日の予定」「3月25日の予定」「昨日の予定」「来月の予定」「今月の予定」「先月の予定」）
- schedule_week: 今週・来週の予定を確認（例: 「今週の予定」「来週の予定」）
- schedule_create: 新しい予定を作成（例: 「明日14時に面談」「毎週月曜に会議」「予定を追加」）※「変更」「削除」「キャンセル」を含まない新規作成のみ
- schedule_search: 特定の予定を検索する（例: 「〇〇の予定っていつ？」「過去の〇〇の会議」「〇〇さんとの打ち合わせ」「〇〇っていつだっけ」）
- schedule_update: 既存の予定を変更・移動する（例: 「会議を14時に変更して」「会議を15時に変更して」「面談を15時にずらして」「明日の面談を来週に変更」「予定を変更したい」「〇〇を〇時に変更」「スケジュール変更」）※「変更」「ずらす」「移動」「リスケ」を含む場合はこれ
- schedule_delete: 予定を削除・キャンセルする（例: 「明日の会議を削除して」「〇〇の予定をキャンセル」「面談を削除して」「予定を消して」「〇〇を取り消して」「会議をなくして」）※カレンダーの予定を削除する場合。タスクではなく予定の削除
- schedule_find_slot: 空いている日時を探す・日程調整（例: 「Bさんと打ち合わせしたいんだけど空いてる日ある？」「来週の空き時間教えて」「〇〇と日程調整して」「打ち合わせしたいんだけど調整して」「他の日時は？」「別の候補は？」）
- task_add: タスクを追加（例: 「タスク追加：報告書作成」「金曜までに〇〇」）
- task_recurring: 繰り返しタスクを登録（例: 「国保連請求 毎月7日」）
- task_list: タスク一覧を確認（例: 「タスク一覧」「今日のタスク」「ルーティン一覧」「毎月のタスク教えて」「毎月の予定教えて」「定期タスク」）
- task_done: タスクを完了にする（例: 「タスク1完了」「報告書作成 完了」）
- task_delete: タスクを削除する（例: 「報告書作成を削除」「タスク1削除」「タスク〇〇を消して」）※「タスク」という単語を含む場合、またはタスク一覧の番号指定の場合のみ
- task_edit: タスクのタイトルや期限を変更する（例: 「タスク1の期限を金曜に変更」「報告書作成の期限を明日に変更」「タスク1を「進捗報告書」に変更」「タスクの名前を変えて」）※タスクの内容を編集する場合
- task_priority: 優先タスクの提案・空き時間の活用相談（例: 「次何やる？」「1時間空いた」「15時から空くけどやることある？」「今やるべきことは？」「暇になった」）
- briefing: 今日のまとめ・ブリーフィング（例: 「今日のまとめ」「ブリーフィング」）
- preference: 設定変更・記憶（例: 「覚えて：〇〇」「設定変更」）
- mail_check: メールを確認（例: 「メール確認」「未読メール」「メールある？」）
- mail_detail: メールの詳細を見る（例: 「メール1の詳細」「メール1を見せて」）
- mail_draft: メールの下書きを作成（例: 「メール1に下書き」「メール1に下書き。〇〇と伝えて」）
- mail_reply: メールに返信して送信（例: 「メール1に返信して」「メール1に返信して。〇〇と伝えて」）
- mail_drafts: 下書き一覧を確認（例: 「下書き一覧」「下書き確認」）
- mail_send: 下書きを送信（例: 「下書き1を送信して」「下書き1送信」）
- summary_report: 過去の活動を振り返る・集計する（例: 「今週何件の面談があった？」「今月のタスク完了数は？」「先週の振り返り」「週報」「月報」「今月の実績」「先月何件予定があった？」）
- help: 使い方やできることを確認（例: 「ヘルプ」「使い方」「何ができる？」「できること」）
- general: 上記に当てはまらない一般的な会話・質問

【ルール】
- インテント名のみを返す（説明不要）
- 「OK」「送信」「はい」などの確認応答は general として返す（会話コンテキストで判定するため）
- 「変更」「ずらす」「リスケ」を含む予定関連 → schedule_update（schedule_createではない）
- 「削除」「キャンセル」「消して」「取り消し」を含む予定関連 → schedule_delete（task_deleteではない）
- task_delete は「タスク」という単語が明示されている場合のみ
- タスクの「変更」「編集」「書き換え」「期限を…に」 → task_edit
- 複数の操作が含まれる場合は、カンマ区切りで複数のインテントを返す（例: 「今日の予定教えて、あとタスク追加して」 → 「schedule_check,task_add」）
- 1つの操作の場合はそのインテント名のみを返す
- 迷った場合は general を選ぶ

ユーザーメッセージ: {user_message}
"""

